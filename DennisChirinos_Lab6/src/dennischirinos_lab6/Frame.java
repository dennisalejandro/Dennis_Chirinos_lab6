    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        RB_JD2_Terminada.setSelected(false);
        int op = JL_JD2_Cliente.getSelectedIndex();
        listClientes.get(op).orden.Terminada = RB_JD2_Terminada.isSelected();
        if (listClientes.get(op).orden.Terminada) {
            double Total = 0;
            for (int i = 0; i < listClientes.get(op).orden.listBaleadas.size(); i++) {
                Total += listClientes.get(op).orden.listBaleadas.get(i).precio;
            }
            System.out.println(Total);
            if (listClientes.get(op).Efectivo >= Total) {
                Cobrar(listClientes.get(op), Total);
                jDialog5.pack();
                jDialog5.setVisible(true);
                if (Afuera < 0) {
                    for (int i = 0; i < listLavaTrastos.size(); i++) {
                        listLavaTrastos.remove(i);
                    }
                } else {
                    Afuera--;
                }
            } else {
                Cliente c = listClientes.get(op);
                listLavaTrastos.add(c);
                JOptionPane.showMessageDialog(rootPane, "No tiene suficiente dinero, AL LAVAPLATOS");
                Afuera = 2;
            }
            listClientes.get(op).orden = null;
        }
    }//GEN-LAST:event_jButton5ActionPerformed

