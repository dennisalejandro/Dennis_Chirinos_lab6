    private void JL_JD2_ClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JL_JD2_ClienteMouseClicked

        DefaultListModel model = (DefaultListModel) JL_JD2_Cliente.getModel();
        int op = JL_JD2_Cliente.getSelectedIndex();
        if (evt.isMetaDown()) {
            POP_UP.pack();
            POP_UP.setLocation(evt.getXOnScreen(), evt.getYOnScreen());
            POP_UP.setVisible(true);
        }
        Cliente cliente = listClientes.get(op);
        DefaultMutableTreeNode Root = new DefaultMutableTreeNode(cliente.getNombre());
        DefaultTreeModel Tree_model = new DefaultTreeModel(Root);
        DefaultMutableTreeNode Nodo_Orden = new DefaultMutableTreeNode("Orden");
        try {
            Nodo_Orden.add(new DefaultMutableTreeNode(cliente.getOrden().Numero));
            SimpleDateFormat SDF = new SimpleDateFormat("YY/MM/DD");
            Nodo_Orden.add(new DefaultMutableTreeNode(SDF.format(cliente.getOrden().Fecha)));
        } catch (NullPointerException e) {
            Nodo_Orden.add(new DefaultMutableTreeNode("Orden Vacia"));
        } catch (Exception e) {
        }

        DefaultMutableTreeNode Nodo_Baleadas = new DefaultMutableTreeNode("Baleadas");
        try {
            for (int i = 0; i < cliente.getOrden().listBaleadas.size(); i++) {
                DefaultMutableTreeNode Nodo_Baleada = new DefaultMutableTreeNode(cliente.getOrden().listBaleadas.get(i));
                for (int j = 0; j < cliente.getOrden().listBaleadas.get(i).Ingredientes.size(); j++) {
                    Nodo_Baleada.add(new DefaultMutableTreeNode(cliente.getOrden().listBaleadas.get(i).Ingredientes.get(j)));
                }
                Nodo_Baleadas.add(Nodo_Baleada);
            }
        } catch (Exception e) {
            Nodo_Orden.add(new DefaultMutableTreeNode("Orden Vacia"));
        }

        Root.add(Nodo_Orden);
        Root.add(Nodo_Baleadas);
        Tree_JD2.setModel(Tree_model);
    }//GEN-LAST:event_JL_JD2_ClienteMouseClicked

