    public void Cobrar(Cliente c, double Total) {
        Orden orden = c.getOrden();
        JL_Fecha.setText(new SimpleDateFormat("YY/MM/DD").format(orden.Fecha));
        JL_Factura.setText(orden.Numero + "");
        JL_Total.setText(Total + "");
        JL_Para.setText(c.Nombre);
        JL_Direccion.setText(c.Direccion);
        JL_Telefono.setText(c.getNúmero() + "");
        String t = "";
        for (int i = 0; i < orden.listBaleadas.size(); i++) {
            t += (orden.listBaleadas.get(i).tipo) + "           " + orden.listBaleadas.get(i).getPrecio() + "\n";
        }
        TA_Factura.setText(t);
    }
